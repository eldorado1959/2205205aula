// Criando um array chamado "numeros" com números inteiros
let numeros = [5, 10, 15, 20, 25];

// Inicializando uma variável para armazenar a soma dos números
let soma = 0;

// Loop para somar todos os números do array
for (let i = 0; i < numeros.length; i++) {
    soma += numeros[i];
}

// Imprimindo o resultado da soma no console
console.log("\nSoma dos números no array (Desafiador):");
console.log("Total: " + soma);


