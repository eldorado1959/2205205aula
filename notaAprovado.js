// Criando uma variável chamada "nota" com um valor numérico representando a nota de um aluno
let nota = 6;

// Usando if / else-if para atribuir uma mensagem de acordo com a nota
if (nota >= 7) {
    console.log("Aprovado");
} else if (nota >= 5 && nota < 7) {
    console.log("Recuperação");
} else {
    console.log("Reprovado");
}
